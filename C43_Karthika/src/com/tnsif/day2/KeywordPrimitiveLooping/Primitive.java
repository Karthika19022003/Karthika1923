package com.tnsif.day2.KeywordPrimitiveLooping;

public class Primitive {

	public static void main(String[] args) {
	int i=19;
	char ch='k';
	double d=19.022003;
	float f=19.2f;

	System.out.println(i);
	System.out.println(ch);
	System.out.println(d);
	System.out.println(f);
	}

}
